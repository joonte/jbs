function AddEdeskNumber(){
	//------------------------------------------------------------------------------
	var numbers = document.querySelectorAll("[data-label=Номер]");
	var themes = document.querySelectorAll("[data-label=Тема]");
	//------------------------------------------------------------------------------
	for (var i = 0; i < themes.length; i++){
		//------------------------------------------------------------------------------
		themes.item(i).attributes['data-label'].value = numbers.item(i).innerText;
		//------------------------------------------------------------------------------
	}
	//------------------------------------------------------------------------------
}
