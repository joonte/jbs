function ctrlEnterEvent(e,IsEdesk){
	//------------------------------------------------------------------------------
	if((e.ctrlKey || e.shiftKey) && (e.keyCode == 10 || e.keyCode == 13)){
		//------------------------------------------------------------------------------
		// если это из тикетницы - ставим флажок
		if(IsEdesk){
			//------------------------------------------------------------------------------
			var form = document.forms['TicketReadForm'];
			if(form.Flags.value == 'No'){
				//------------------------------------------------------------------------------
				form.Flags.value = 'CloseOnSee';
				//------------------------------------------------------------------------------
			}
			//------------------------------------------------------------------------------
		}
		//------------------------------------------------------------------------------
		return true;
		//------------------------------------------------------------------------------
	}else{
		//------------------------------------------------------------------------------
		return false;
		//------------------------------------------------------------------------------
	}
}
