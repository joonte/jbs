	public function fiscalCheck($Invoice,$Positions,$moneyType = 2){
		#-------------------------------------------------------------------------------
		$Query = Array('document'=>Array());
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		$this->Summ = 0;
		#-------------------------------------------------------------------------------
		$sessionId = $this->makeSessionId($Invoice['ID']);
