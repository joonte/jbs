	// сохранение сопоставления между номером чека и иденфтикатором SessionId, может когда-то понадобится
	private function SavedocNumber($SessionId,$docNumber){
		#-------------------------------------------------------------------------------
		$FileDB = $this->CreateThreadDB();
		#-------------------------------------------------------------------------------
		// сохраняем переданные данные
		$IsWrite = IO_Write($FileDB,SPrintF("%s\t%s\n",$docNumber,$SessionId));
		if(Is_Error($IsWrite))
			return ERROR | @Trigger_Error(500);
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		return TRUE;
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
	}


	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	// создаём файл с базой соответствия идентфиикаторов чеков и номеров счетов
	private function CreateThreadDB(){
		#-------------------------------------------------------------------------------
		$Tmp = System_Element('tmp');
		if(Is_Error($Tmp))
			return ERROR | @Trigger_Error('[CreateThreadDB_Get_Tmp]: не удалось найти временную папку');
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		$FileDB = SPrintF('%s/Taxation.txt',$Tmp);
		#-------------------------------------------------------------------------------
		if(!File_Exists($FileDB)){
			#-------------------------------------------------------------------------------
			$IsWrite = IO_Write($FileDB,"#docNumber\tSessionId\n");
			if(Is_Error($IsWrite))
				return ERROR | @Trigger_Error(500);
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		return $FileDB;
		#-------------------------------------------------------------------------------
