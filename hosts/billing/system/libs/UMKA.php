	private function makeFiscProp($Data,$Tag = FALSE){
		#-------------------------------------------------------------------------------
		$Result = Array('fiscprops'=>Array());
		#-------------------------------------------------------------------------------
		foreach($Data as $Item)
			$Result['fiscprops'][] = $Item;
		#-------------------------------------------------------------------------------
		if($Tag)
			$Result['tag'] = $Tag;
		#-------------------------------------------------------------------------------
		return $Result;
		#-------------------------------------------------------------------------------
	}
 
