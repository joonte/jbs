        // АПИ
        private function API($Method,$Query = Array(),$Attachment = Array(),$Url = FALSE,$IsDelete = FALSE){
		#-------------------------------------------------------------------------------
		$HTTP = Array(
				'Port'		=> 443,
				'Protocol'	=> 'ssl',
				);
		#-------------------------------------------------------------------------------
		if(!$Url){
			#-------------------------------------------------------------------------------
			$HTTP['Address']	= $this->Address;
			$HTTP['Host']		= $this->Host;
			#-------------------------------------------------------------------------------
			$Url = SPrintF('/%s',$Method);
			#-------------------------------------------------------------------------------
			if($IsDelete)
				$HTTP['Method'] = 'DELETE';
			#-------------------------------------------------------------------------------
			//$Query['Authorization']	= $this->Token;
			#-------------------------------------------------------------------------------
/*			// для уникальности сообщения. пока так, рандомно
			if($Method == 'messages')
				$Query['random_id']	= IntVal(MicroTime(TRUE) * 10000);
*/			#-------------------------------------------------------------------------------
		}else{
			#-------------------------------------------------------------------------------
			// загрузка фоток/документов идёт по отдельному УРЛ
			$Parts = Parse_Url($Url);
			#-------------------------------------------------------------------------------
			$HTTP['Address']	= $Parts['host'];
			$HTTP['Host']		= $Parts['host'];
			#-------------------------------------------------------------------------------
			$Url = SPrintF('%s?%s',$Parts['path'],$Parts['query']);
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		if($Method == 'file' || $Method == 'image'){
			#-------------------------------------------------------------------------------
			Debug(SPrintF('[system/libs/MAX:API]: обработка вложения (%s), размер (%s), тип (%s)',$Attachment['Name'],$Attachment['Size'],$Attachment['Mime']));
			#-------------------------------------------------------------------------------
			// имя поля в форме
			$FieldName = ($Method == 'image')?'image':'file';
			#-------------------------------------------------------------------------------
			$HTTP['Charset'] = '';
			#-------------------------------------------------------------------------------
			$Boundary = SPrintF('----%s',Md5(Rand()));
			#-------------------------------------------------------------------------------
			$Body = SPrintF("--%s\r\n",$Boundary);
			$Body = SPrintF("%sContent-Disposition: form-data; name=\"%s\"; filename=\"%s\"\r\n",$Body,$FieldName,$Attachment['Name']);
			$Body = SPrintF("%sContent-Type: application/octet-stream\r\n",$Body);
			$Body = SPrintF("%sContent-Transfer-Encoding: binary\r\n",$Body);
			$Body = SPrintF("%s\r\n%s",$Body,Base64_Decode($Attachment['Data']));
			$Body = SPrintF("%s\r\n--%s--\r\n\r\n",$Body,$Boundary);
			#-------------------------------------------------------------------------------
			$Headers = Array(SPrintF('Content-Type: multipart/form-data; boundary=%s',$Boundary)/*,'Connection: keep-alive','Keep-Alive: 300'*/);
			#-------------------------------------------------------------------------------
		}else{
			#-------------------------------------------------------------------------------
			// без файла передаём через POST
			$Headers	= Array(SPrintF('Authorization: %s',$this->Token));
			$Body		= SizeOf($Query)?Json_Encode($Query):' ';	// пробел, чтобы через POST ушло
			$Query		= Array();
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		$Result = HTTP_Send($Url,$HTTP,$Query,$Body,$Headers);
		#-------------------------------------------------------------------------------
		if(Is_Error($Result))
			return ERROR | @Trigger_Error('[API]: не удалось выполнить запрос к серверу');
		#-------------------------------------------------------------------------------
		$Result = Trim($Result['Body']);
		#-------------------------------------------------------------------------------
		$Result = Json_Decode($Result,TRUE);
		#-------------------------------------------------------------------------------
		Debug(SPrintF('[system/libs/MAX:API]: ответ сервера MAX: %s',print_r($Result,true)));
		#-------------------------------------------------------------------------------
		// это сообщение, возвращаем идентфикатор отправленного сообщения
		if(IsSet($Result['message']['body']['mid']))
			return $Result['message']['body']['mid'];
		#-------------------------------------------------------------------------------
		// это запрос URL для загрузки, возвращаем его
		if(IsSet($Result['url']))
			return $Result['url'];
		#-------------------------------------------------------------------------------
		// результат загрузки фотографии
		if(IsSet($Result['photos']))
			foreach($Result['photos'] as $Photo)
				return $Photo['token'];
		#-------------------------------------------------------------------------------
		// результат загрузки файла
		if(IsSet($Result['token']))
			return $Result['token'];
		#-------------------------------------------------------------------------------

		#-------------------------------------------------------------------------------
		// неизвестно что
		Debug(SPrintF('[system/libs/MAX:API]: неизвестный ответ сервера MAX'));
		#-------------------------------------------------------------------------------
		return FALSE;
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
	}

