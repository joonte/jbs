	// отправка файла
	public function FileSend($MaxUserID,$Attachments = Array()){
		#-------------------------------------------------------------------------------
		#Debug(SPrintF('[system/libs/MAX:FileSend]: Attachments = %s',print_r($Attachments,true)));
		#-------------------------------------------------------------------------------
		// массив под идентифкаторы отправленных сообщений
		$Array = Array();
		#-------------------------------------------------------------------------------
		foreach ($Attachments as $Attachment){
			#-------------------------------------------------------------------------------
			Debug(SPrintF('[system/libs/MAX:FileSend]: обработка вложения (%s), размер (%s), тип (%s)',$Attachment['Name'],$Attachment['Size'],$Attachment['Mime']));
			#-------------------------------------------------------------------------------
			// по дефолту, метод для документов
			$Method = 'file';
			#-------------------------------------------------------------------------------
			// если это картинка - меняем метод
			$Mime = Explode('/',$Attachment['Mime']);
			#-------------------------------------------------------------------------------
			if(In_Array($Attachment['Mime'],Array('image/png','image/gif','image/jpeg','image/tiff')))
				$Method = 'image';
			#-------------------------------------------------------------------------------
			#-------------------------------------------------------------------------------
			// получаем адрес на который надо отослать файл
			$Url = $this->API(SPrintF('uploads?type=%s',$Method));
