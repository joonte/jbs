	// регистрируем WEB-hook для бота
	public function SetWebHook(){
		#-------------------------------------------------------------------------------
		// строим URL
		$Url = SPrintF('https://%s/API/MAX?Secret=%s',HOST_ID,$this->Secret);
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		// проверяем веб-хук
		$Result = $this->API('subscriptions');
		#-------------------------------------------------------------------------------
		Debug(SPrintF('[system/libs/MAX:SetWebHook]: $Result = %s',print_r($Result,true)));
		#-------------------------------------------------------------------------------
		// подписки есть, проверяем их
		if(IsSet($Result['subscriptions']) && Is_Array($Result['subscriptions']) && SizeOf($Result['subscriptions']) > 0){
			#-------------------------------------------------------------------------------
			// перебираем веб-хуки, проверяем соответствие тому который должен быть
			foreach($Result['subscriptions'] as $Subscription){
				#-------------------------------------------------------------------------------
				// проверяем домен
				if(!Preg_Match(SPrintF('#https://%s#',HOST_ID),$Subscription['url']))
					continue;
				#-------------------------------------------------------------------------------
				// проверяем адрес
				if($Subscription['url'] != $Url){
					#-------------------------------------------------------------------------------
					// домен совпал, а URL - нет. надо удалить.
					$Result = $this->API(SPrintF('subscriptions?url=%s',$Subscription['url']),Array(),Array(),FALSE,TRUE);
					#-------------------------------------------------------------------------------
				}else{
					#-------------------------------------------------------------------------------
					Debug(SPrintF('[system/libs/MAX:SetWebHook]: Веб-хук установлен и соответствует заданнному в настройках: %s',$Url));
					#-------------------------------------------------------------------------------
					return TRUE;
					#-------------------------------------------------------------------------------
				}
				#-------------------------------------------------------------------------------
			}
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		// если не установле или ошибка - устанавливаем заново
		$Query = Array('url'=>$Url,/*'update_types'=>'message_created,bot_added,user_added,user_removed,bot_removed,bot_started',*/);
		#-------------------------------------------------------------------------------
		$Result = $this->API('subscriptions',$Query);
		#-------------------------------------------------------------------------------
		Debug(SPrintF('[system/libs/MAX:SetWebHook]: $Result = %s',print_r($Result,true)));
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		if(IsSet($Result['ok']) && $Result['ok']){
			#-------------------------------------------------------------------------------
			return TRUE;
			#-------------------------------------------------------------------------------
		}else{
			#-------------------------------------------------------------------------------
			// TODO по идее там есть человекочитемое сообщение о ошибке. надо словить и выдать в ответе
			Debug(SPrintF('[system/libs/MAX:SetWebHook]: $Result = %s',print_r($Result,true)));
			#-------------------------------------------------------------------------------
			return FALSE;
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
	}

