	// поиск тикета по номеру сообщения в телеграмме
	public function FindThreadID($MaxMessageID){
		#-------------------------------------------------------------------------------
		$Thread = DB_Select('TmpData','*',Array('UNIQ','Where'=>Array('`AppID` = "MAX"',SPrintF('`Col3` = "%s"',$MaxMessageID)),'SortOn'=>'CreateDate','Limits'=>Array(0,1)));
		#-------------------------------------------------------------------------------
		switch(ValueOf($Thread)){
		case 'error':
			return ERROR | @Trigger_Error(500);
		case 'exception':
			return FALSE;
		case 'array':
			#-------------------------------------------------------------------------------
			return Array('UserID'=>$Thread['UserID'],'TicketID'=>$Thread['Col1'],'MessageID'=>$Thread['Col2']);
			#-------------------------------------------------------------------------------
		default:
			return ERROR | @Trigger_Error(101);
		}    
		#-------------------------------------------------------------------------------
	}
