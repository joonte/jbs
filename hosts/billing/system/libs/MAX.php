				Debug(SPrintF('[system/libs/MAX:FileSend]: не удалось получить URL для отправки файла'));
				#-------------------------------------------------------------------------------
				continue;
				#-------------------------------------------------------------------------------
			}
			#-------------------------------------------------------------------------------
			#-------------------------------------------------------------------------------
			// отправляем файл на заданный URL
			$Result = $this->API($Method,Array(),$Attachment,$Url);
			#-------------------------------------------------------------------------------
			if(!$Result){
				#-------------------------------------------------------------------------------
				Debug(SPrintF('[system/libs/MAX:FileSend]: не удалось отправить файл %s',$Method));
				#-------------------------------------------------------------------------------
				continue;
				#-------------------------------------------------------------------------------
			}
			#-------------------------------------------------------------------------------
			#-------------------------------------------------------------------------------
			// ждём, иногда не сразу обрабатывает вложение
			Sleep(1);
			#-------------------------------------------------------------------------------
			// отправляем сообщение с вложением
			$Result = $this->API(SPrintF('messages?user_id=%u',$MaxUserID),Array('attachments'=>[Array('type'=>$Method,'payload'=>Array('token'=>$Result))]));
			#-------------------------------------------------------------------------------
			if($Result){
				#-------------------------------------------------------------------------------
				$Array[] = $Result;
				#-------------------------------------------------------------------------------
				continue;
				#-------------------------------------------------------------------------------
			}else{
				#-------------------------------------------------------------------------------
				Debug(SPrintF('[system/libs/MAX:FileSend]: $Result = %s',print_r($Result,true)));
