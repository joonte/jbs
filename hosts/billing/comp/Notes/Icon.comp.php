if(IsSet($GLOBALS['__USER'])){
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load('Notes','User');
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	// если уведомления есть, то звонок - цветной
	if(SizeOf($Comp->Childs)){
		#-------------------------------------------------------------------------------
		return new Tag('NOBODY',new Tag('IMG',Array('width'=>'20px','src'=>'SRC:{Images/Icons/BellRed.png}','onmouseover'=>SPrintF("PromptShow(event,'У вас %u новых уведомлений',this);",SizeOf($Comp->Childs)))),$Adding);
		#-------------------------------------------------------------------------------
	}else{
		#-------------------------------------------------------------------------------
		return new Tag('NOBODY',new Tag('IMG',Array('width'=>'20px','src'=>'SRC:{Images/Icons/BellGray.png}','onmouseover'=>"PromptShow(event,'Нет уведомлений',this);")),$Adding);
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Adding;
