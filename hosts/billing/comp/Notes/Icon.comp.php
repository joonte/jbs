$__args_list = Array('Adding');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
if(IsSet($GLOBALS['__USER'])){
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load('Notes','User');
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	// цвет иконки
	$Color	= (SizeOf($Comp->Childs))?'Red':'Gray';
	#-------------------------------------------------------------------------------
	// текст подсказки
	$Text	= (SizeOf($Comp->Childs))?SPrintF('Новых уведомлений: %u шт.',SizeOf($Comp->Childs)):'Нет новых уведомлений';
	#-------------------------------------------------------------------------------
	$Text	= SPrintF("PromptShow(event,'%s',this);",$Text);
	#-------------------------------------------------------------------------------
	$A = new Tag('A',Array('href'=>'/Notes','class'=>'Image','onmouseover'=>$Text),new Tag('IMG',Array('width'=>'20px','src'=>SPrintF('SRC:{Images/Icons/Bell%s.png}',$Color))),$Adding);
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	return new Tag('NOBODY',$A,$Adding);
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Adding;
