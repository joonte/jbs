<?php

#-------------------------------------------------------------------------------
/** @author Кунич А.С. */
/******************************************************************************/
/******************************************************************************/
$__args_list = Array('SystemID','InvoiceID','Summ');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Config = Config();
#-------------------------------------------------------------------------------
$Settings = $Config['Invoices']['PaymentSystems']['OnPay'];
#-------------------------------------------------------------------------------
$Send = $Settings['Send'];
#-------------------------------------------------------------------------------
$Send['pay_for'] = $InvoiceID;
#-------------------------------------------------------------------------------
$Send['f'] = 11;
#-------------------------------------------------------------------------------
$PriceFinal = true;
#-------------------------------------------------------------------------------
if($PriceFinal)
	$Send['price_final'] = 'true';
#-------------------------------------------------------------------------------
$Send['url_success']  = SPrintF('%s://%s/Invoices',URL_SCHEME,HOST_ID);
#-------------------------------------------------------------------------------
$Send['url_fail']  = SPrintF('%s://%s/Invoices?Error=yes',URL_SCHEME,HOST_ID);
#-------------------------------------------------------------------------------
$Send['price'] = Round($Summ/$Settings['Course'],2);
#-------------------------------------------------------------------------------
$Send['price_md5'] = Number_Format($Send['price'], 2, '.', '');
#-------------------------------------------------------------------------------
if(SubStr($Send['price_md5'], -1) == "0")
	$Send['price_md5'] = Number_Format($Send['price'], 1, '.', ''); 
#-------------------------------------------------------------------------------
$Send['ticker'] = 'RUB';
#-------------------------------------------------------------------------------
$Send['convert'] = 'yes';
#-------------------------------------------------------------------------------
$Hash = Array(
	$Send['pay_mode'],
	$Send['price_md5'],
	$Send['ticker'],
	$Send['pay_for'],
	$Send['convert'],
	$Settings['MerchantPass']
);
#-------------------------------------------------------------------------------
$Send['md5'] = Md5(Implode(';',$Hash));
#-------------------------------------------------------------------------------
$Comp = Comp_Load('Formats/Invoice/Number',$InvoiceID);
if(Is_Error($Comp))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$__USER = $GLOBALS['__USER'];
#-------------------------------------------------------------------------------
# выбираем телефон юзера, первый из списка, если он вообще есть
foreach($__USER['Contacts'] as $Contact)
	if($Contact['MethodID'] == 'SMS')
		$Send['user_phone'] = $Contact['Address'];
#-------------------------------------------------------------------------------
$Send['user_email'] = $__USER['Email'];
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Send;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
