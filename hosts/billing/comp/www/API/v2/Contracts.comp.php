<?php
#-------------------------------------------------------------------------------
/** @author Alex Keda, for www.host-food.ru */
/******************************************************************************/
/******************************************************************************/
$__args_list = Array('Args');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Args = IsSet($Args)?$Args:Args();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(Is_Error(System_Load('modules/Authorisation.mod')))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Config = Config();
#-------------------------------------------------------------------------------
$PaymentSystems = $Config['Invoices']['PaymentSystems'];
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Out = Array();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Contracts = DB_Select('ContractsOwners',Array('*'),Array('Where'=>SPrintF('`UserID` = %u',$GLOBALS['__USER']['ID'])));
#-------------------------------------------------------------------------------
switch(ValueOf($Contracts)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	return $Out;
case 'array':
	#-------------------------------------------------------------------------------
	foreach($Contracts as $Contract){
		#-------------------------------------------------------------------------------
		$Count = DB_Count('OrdersOwners',Array('Where'=>SPrintF('`ContractID` = %u',$Contract['ID'])));
		if(Is_Error($Count))
			return ERROR | @Trigger_Error(500);
		#-------------------------------------------------------------------------------
		$Contract['Orders'] = $Count;
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		$Contract['PaymentSystems'] = Array();
		#-------------------------------------------------------------------------------
		// список платёжных систем, которыми можно оплачивать по этому договору
		foreach(Array_Keys($PaymentSystems) as $PaymentSystemID){
			#-------------------------------------------------------------------------------
			$PaymentSystem = $PaymentSystems[$PaymentSystemID];
			#-------------------------------------------------------------------------------
			// неактивные - пропускаем
			if(!$PaymentSystem['IsActive'])
				continue;
			#-------------------------------------------------------------------------------
			if($Contract['TypeID'] == 'Default'){
				#-------------------------------------------------------------------------------
				// любые активные
				$Contract['PaymentSystems'][] = $PaymentSystemID;
				#-------------------------------------------------------------------------------
			}else{
				#-------------------------------------------------------------------------------
				if(IsSet($PaymentSystem['ContractsTypes'][$Contract['TypeID']]) && $PaymentSystem['ContractsTypes'][$Contract['TypeID']])
					$Contract['PaymentSystems'][] = $PaymentSystemID;
				#-------------------------------------------------------------------------------
			}
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		$Out[$Contract['ID']] = $Contract;
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
	break;
	#-------------------------------------------------------------------------------
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Out;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
