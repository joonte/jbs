<?php
#-------------------------------------------------------------------------------
/** @author Alex Keda, for www.host-food.ru */
/******************************************************************************/
/******************************************************************************/
$__args_list = Array('Args');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Args = IsSet($Args)?$Args:Args();
#-------------------------------------------------------------------------------
$Partition	= (string) @$Args['Partition'];
#-------------------------------------------------------------------------------
if(Is_Error(System_Load('classes/DOM.class.php')))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
// некоторые страницы без авторизации показываем
$IsAuth = TRUE;
#-------------------------------------------------------------------------------
$Regulars = Array('/Contacts','News','Contracts/(.*)/Content');
#-------------------------------------------------------------------------------
foreach($Regulars as $Regular)
	if(Preg_Match(SPrintF('#%s#',$Regular),$Partition))
		$IsAuth = FALSE;
#-------------------------------------------------------------------------------
if($IsAuth)
	if(Is_Error(System_Load('modules/Authorisation.mod')))
		return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Out = Array();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Clauses = DB_Select('ClausesOwners',Array('*'),Array('Where'=>SPrintF('`IsPublish` = "yes"')));
#-------------------------------------------------------------------------------
switch(ValueOf($Clauses)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	return $Out;
case 'array':
	break;
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
// Переменные
$Replace = Array('HOST_ID'=>HOST_ID);
#-------------------------------------------------------------------------------
if(IsSet($GLOBALS['__USER']))
	$Replace['__USER'] = $GLOBALS['__USER'];
#-------------------------------------------------------------------------------
// JBS-1446: add patterns
$Executor = Comp_Load('www/Administrator/API/ProfileCompile',Array('ProfileID'=>100));
#-------------------------------------------------------------------------------
switch(ValueOf($Executor)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	break;
case 'array':
	#-------------------------------------------------------------------------------
	$Replace['Executor'] = $Executor['Attribs'];
	#-------------------------------------------------------------------------------
	break;
	#-------------------------------------------------------------------------------
default:
	return ERROR | @Trigger_Error(100);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
foreach($Clauses as $Clause){
	#-------------------------------------------------------------------------------
	// TODO по уму, надо массив с шаблонами и перебирать их. и выпилить не тока админские но и всё лишнее.
	// или колонку в БД, что статья видна по АПИ.
	$Regulars = Array('Administrator','Registrators','Invoices/','Envelopes/');
	#-------------------------------------------------------------------------------
	foreach($Regulars as $Regular)
		if(Preg_Match(SPrintF('#%s#',$Regular),$Clause['Partition']))
			continue 2;
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	// если запрашивают не конкретную статью, то удаляем ещё лишнее
	$Regulars = Array('Contracts/');
	#-------------------------------------------------------------------------------
	if(!$Partition)
		foreach($Regulars as $Regular)
			if(Preg_Match(SPrintF('#%s#',$Regular),$Clause['Partition']))
				continue 2;
	#-------------------------------------------------------------------------------
	UnSet($Clause['UserID']);
	UnSet($Clause['AuthorID']);
	UnSet($Clause['EditorID']);
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	// Переменные
	$Replace['Clause'] = Array('ID'=>$Clause['ID']);
	#-------------------------------------------------------------------------------
	$Replace1 = Array_ToLine($Replace,'%');
	#-------------------------------------------------------------------------------
	foreach(Array_Keys($Replace1) as $Key){
		#-------------------------------------------------------------------------------
		if(Is_Null($Replace1[$Key])){
			#-------------------------------------------------------------------------------
			#Debug(SPrintF('[comp/www/API/v2/Clauses]: $Key = %s value is NULL',$Key));
			#-------------------------------------------------------------------------------
			$Replace1[$Key] = "";
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		$Clause['Text'] = Str_Replace($Key,$Replace1[$Key],$Clause['Text']);
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	// в статьях есть компоненты. надо пробовать грузить
	if(Preg_Match('#\<COMP\s#',$Clause['Text'])){
		#-------------------------------------------------------------------------------
		$XML = String_XML_Parse($Clause['Text']);
		if(Is_Exception($XML))
			return ERROR | @Trigger_Error(500);
		#-------------------------------------------------------------------------------
		$XML = Current($XML->Childs);
		#-------------------------------------------------------------------------------
		$DOM = new DOM();
		$Links = &Links();
		$Links['DOM'] = &$DOM;
		#-------------------------------------------------------------------------------
		if(Is_Error($DOM->Load('Blank')))
			return ERROR | @Trigger_Error(500);
		#-------------------------------------------------------------------------------
		$DOM->AddChild('Body',$XML);
		#-------------------------------------------------------------------------------
		$DOM = $DOM->Build();
		#-------------------------------------------------------------------------------
		Preg_Match('/<body[^>]*>(.*?)<\/body>/is',$DOM,$Matches);
		#-------------------------------------------------------------------------------
		$Clause['TextOrig'] = $Clause['Text'];
		$Clause['Text'] = $Matches[1];
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	if($Partition){
		#-------------------------------------------------------------------------------
		Debug(SPrintF('[comp/www/API/v2/Clauses]: Compare: %s | %s',$Partition,$Clause['Partition']));
		// задана конкретная статья
//		if($Clause['Partition'] == Trim($Partition) || SubStr_Count($Clause['Partition'],Trim($Partition)))
		if($Clause['Partition'] == Trim($Partition)){
			#-------------------------------------------------------------------------------
			$Out[$Clause['ID']] = $Clause;
			#-------------------------------------------------------------------------------
		}elseif(Trim($Partition) == 'News' && $Clause['GroupID'] == 2){
			#-------------------------------------------------------------------------------
			// новости они по разделу в первую очередь
			$Out[$Clause['ID']] = $Clause;
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
	}else{
		#-------------------------------------------------------------------------------
		$Out[$Clause['ID']] = $Clause;
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Out;
#-------------------------------------------------------------------------------
