/******************************************************************************/
/******************************************************************************/
$__args_list = Array('Args');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Args = IsSet($Args)?$Args:Args();
#-------------------------------------------------------------------------------
$Partition	= (string) @$Args['Partition'];
#-------------------------------------------------------------------------------
// договора без авторизации показываем
if($Partition != '/Contacts' || $Partition != '/Content')
	if(Is_Error(System_Load('modules/Authorisation.mod')))
		return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Out = Array();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Clauses = DB_Select('ClausesOwners',Array('*'),Array('Where'=>SPrintF('`IsPublish` = "yes"')));
#-------------------------------------------------------------------------------
switch(ValueOf($Clauses)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	return $Out;
case 'array':
	break;
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
// Переменные
$Replace = Array('HOST_ID'=>HOST_ID);
#-------------------------------------------------------------------------------
if(IsSet($GLOBALS['__USER']))
	$Replace['__USER'] = $GLOBALS['__USER'];
#-------------------------------------------------------------------------------
// JBS-1446: add parrerns
$Executor = Comp_Load('www/Administrator/API/ProfileCompile',Array('ProfileID'=>100));
#-------------------------------------------------------------------------------
switch(ValueOf($Executor)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	break;
case 'array':
	#-------------------------------------------------------------------------------
	$Replace['Executor'] = $Executor['Attribs'];
	#-------------------------------------------------------------------------------
	break;
	#-------------------------------------------------------------------------------
default:
	return ERROR | @Trigger_Error(100);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
foreach($Clauses as $Clause){
	#-------------------------------------------------------------------------------
	// TODO по уму, надо массив с шаблонами и перебирать их. и выпилить не тока админские но и всё лишнее.
	// или колонку в БД, что статья видна по АПИ.
	// выпливаем админские
	if(Preg_Match('/Administrator/',$Clause['Partition']))
		continue;
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	UnSet($Clause['UserID']);
	UnSet($Clause['AuthorID']);
	UnSet($Clause['EditorID']);
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	// обработка текста
#	$Comp = Comp_Load('Clauses/Load',$Clause['ID'],TRUE,TRUE);
#	if(Is_Error($Comp))
#		return ERROR | @Trigger_Error(500);
#	#-------------------------------------------------------------------------------
#	$Clause['Text'] = $Comp;
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	// Переменные
	$Replace['Clause'] = Array('ID'=>$Clause['ID']);
	#-------------------------------------------------------------------------------
	$Replace1 = Array_ToLine($Replace,'%');
	#-------------------------------------------------------------------------------
	foreach(Array_Keys($Replace1) as $Key){
		#-------------------------------------------------------------------------------
		if(Is_Null($Replace1[$Key])){
			#-------------------------------------------------------------------------------
			#Debug(SPrintF('[comp/www/API/v2/Clauses]: $Key = %s value is NULL',$Key));
			#-------------------------------------------------------------------------------
			$Replace1[$Key] = "";
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		$Clause['Text'] = Str_Replace($Key,$Replace1[$Key],$Clause['Text']);
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
	if($Partition){
		#-------------------------------------------------------------------------------
		// задана конкретная статья
//		if($Clause['Partition'] == Trim($Partition) || SubStr_Count($Clause['Partition'],Trim($Partition)))
		if($Clause['Partition'] == Trim($Partition))
			$Out[$Clause['ID']] = $Clause;
		#-------------------------------------------------------------------------------
	}else{
		#-------------------------------------------------------------------------------
		$Out[$Clause['ID']] = $Clause;
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Out;
