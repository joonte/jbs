$__args_list = Array('Args');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Args = IsSet($Args)?$Args:Args();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(Is_Error(System_Load('modules/Authorisation.mod')))
        return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(!IsSet($GLOBALS['__USER']))
	return Array();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$UsersIPs = DB_Select('UsersIPs',Array('FROM_UNIXTIME(`CreateDate`) AS `Date`','IP','UA'),Array('Where'=>'`UserID` = @local.__USER_ID','SortOn'=>'CreateDate','IsDesc'=>TRUE));
#-------------------------------------------------------------------------------
switch(ValueOf($UsersIPs)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	return Array();
case 'array':
	break;
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return($UsersIPs);
