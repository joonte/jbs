<?php

#-------------------------------------------------------------------------------
/** @author Alex Keda, for www.host-food.ru */
/******************************************************************************/
/******************************************************************************/
$__args_list = Array('Args');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Args = IsSet($Args)?$Args:Args();
#-------------------------------------------------------------------------------
$ServiceOrderID		= (integer) @$Args['ServiceOrderID'];
$ServiceOrderType	=  (string) @$Args['ServiceOrderType'];
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if($ServiceOrderType == 'Default'){
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load('www/ServiceOrderPay',Array('ServiceOrderID'=>$ServiceOrderID));
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
}else{
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load(SPrintF('www/%sOrderPay',$ServiceOrderType),Array('OrderID'=>$ServiceOrderID));
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Comp;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
