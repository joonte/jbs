/******************************************************************************/
/******************************************************************************/
$__args_list = Array('ShowWindow','LinkText','Length');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
#-------------------------------------------------------------------------------
$Config = Config();
#-------------------------------------------------------------------------------
$Settings = $Config['Other']['Eval'];
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Link = SPrintF('%s://%s/Home?Eval=%s',Url_Scheme(),HOST_ID,Crypt_Encode($ShowWindow,$Settings['EvalKey']));
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
// если нужна только ссылка, вертаем до создания линка с текстом
if(!$LinkText)
	return $Link;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Name = Comp_Load('Formats/String',$LinkText,($Length)?$Length:10,$Link);
if(Is_Error($Name))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Name;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
