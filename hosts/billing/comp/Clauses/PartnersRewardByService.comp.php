/******************************************************************************/
/******************************************************************************/
$__args_list = Array('NotUsed');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
# достаём список всех сервисов
$Where = Array(
		 "`IsHidden` = 'no'",
		 "`IsActive` = 'yes'",
);
#-------------------------------------------------------------------------------
$Services = DB_Select('ServicesOwners',Array('ID','Name','PartnersRewardPercent'),Array('Where'=>$Where,'SortOn'=>'SortID'));
switch(ValueOf($Services)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	#---------------------------------------------------------------------------
	$Comp = Comp_Load('Information','Активных сервисов не обнаружено','Notice');
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#---------------------------------------------------------------------------
	return $Comp;
	#-------------------------------------------------------------------------------
case 'array':
	#-------------------------------------------------------------------------------
	$NoBody = new Tag('NOBODY');
	#-------------------------------------------------------------------------------
	//$NoBody->AddChild(new Tag('H2','Процент отчислений с заказов услуг'));
	#-------------------------------------------------------------------------------
	break;
	#-------------------------------------------------------------------------------
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$NoBody->AddChild(new Tag('BR'));
# таблица для списка услуг
$Table = Array();
#-------------------------------------------------------------------------------
$Tr = new Tag('TR');
$Tr->AddChild(new Tag('TD',Array('class'=>'Head','align'=>'center','bgcolor'=>'#B9CCDF'),new Tag('SPAN','Услуга')));
$Tr->AddChild(new Tag('TD',Array('class'=>'Head','align'=>'center','bgcolor'=>'#B9CCDF'),new Tag('SPAN','Вознаграждение')));
$Table[] = $Tr;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
# перебираем сервисы
foreach($Services as $Service){
	#-------------------------------------------------------------------------------
	$Tr = new Tag('TR');
	#-------------------------------------------------------------------------------
	$Tr->AddChild(new Tag('TD',Array('align'=>'left','class'=>'Standard'),new Tag('SPAN',$Service['Name'])));
	#-------------------------------------------------------------------------------
	$Tr->AddChild(new Tag('TD',Array('align'=>'left','class'=>'Standard'),new Tag('SPAN',SPrintF('%s%%',$Service['PartnersRewardPercent']))));
	#-------------------------------------------------------------------------------
	$Table[] = $Tr;
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
$Comp = Comp_Load('Tables/Extended',$Table,Array('width'=>'100%','border'=>1,'cellspacing'=>0,'style'=>'border: 1px black; border-collapse: collapse;'));
if(Is_Error($Comp))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$NoBody->AddChild(new Tag('DIV',Array('align'=>'center'),$Comp));
//$NoBody->AddChild($Comp);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
# возвращаем документ
return $NoBody;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
