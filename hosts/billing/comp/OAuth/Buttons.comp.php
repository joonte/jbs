/******************************************************************************/
/******************************************************************************/
$__args_list = Array('JS');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Config = Config();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
// DIV с кнопками внешней авторизации
$Div = new Tag('DIV',Array('class'=>'SocialLoginIcons'));
#-------------------------------------------------------------------------------
// варианты внешней авторизации
$OAuth = $Config['Interface']['User']['OAuth'];
#-------------------------------------------------------------------------------
foreach(Array_Keys($OAuth) as $Key){
	#-------------------------------------------------------------------------------
	if(!$OAuth[$Key]['IsActive'])
		continue;
	#-------------------------------------------------------------------------------
	// ссылка/кнопка
	$A = new Tag('A',Array('class'=>SPrintF('Social%s',$Key),'OnClick'=>SPrintF("JavaScript:window.open('%s','OAuth_window').focus();%s;",SPrintF($OAuth[$Key]['OauthURL'],$OAuth[$Key]['ClientID']),$JS),'target'=>'_blank'),new Tag('I',Array('class'=>SPrintF('Social_%s',$Key)),' '));
	#-------------------------------------------------------------------------------
	// добавляем в список
	$Div->AddChild($A);
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Div;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
?>
