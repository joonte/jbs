/******************************************************************************/
/******************************************************************************/
$__args_list = Array('ID','ServiceID');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Out = Array();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
// достаём поля услуги
$ServicesFields = DB_Select('ServicesFieldsOwners','*',Array('Where'=>SPrintF('`ServiceID` = %u',$ServiceID),'SortOn'=>'SortID'));
switch(ValueOf($ServicesFields)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	return $Out;
case 'array':
	break;
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
// перебираем поля, достаём значения
foreach($ServicesFields as $ServicesField){
	#-------------------------------------------------------------------------------
	// достаём значениия дл яэтого поля
	$OrdersFields = DB_Select('OrdersFieldsOwners','*',Array('Where'=>Array(SPrintF('`ServiceFieldID` = %u',$ServicesField['ID']),SPrintF('`OrderID` = %u',$ID))));
	switch(ValueOf($OrdersFields)){
	case 'error':
		return ERROR | @Trigger_Error(500);
	case 'exception':
		continue 2;
	case 'array':
		break;
	default:
		return ERROR | @Trigger_Error(101);
	}
	#-------------------------------------------------------------------------------
	foreach($OrdersFields as $OrdersField){
		#-------------------------------------------------------------------------------
		// если поле не найдено, то покажем хотя бы ключ
		$Value = SPrintF('Key:%s',$OrdersField['Value']);
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		$Lines = Explode("\n",$ServicesField['Options']);
		foreach($Lines as $Line){
			#-------------------------------------------------------------------------------
			//Debug(SPrintF('[comp/Services/Orders/GetOrderKeys]: $Line = %s',$Line));
			$Keys = Explode("=",$Line);
			#-------------------------------------------------------------------------------
			// сдвигаем указатель на начало массива
			Reset($Keys);
			#-------------------------------------------------------------------------------
			//Debug(SPrintF('[comp/Services/Orders/GetOrderKeys]: Keys[0] = %s; Keys[1] = %s; Keys[2] = %s',$Keys[0],$Keys[1],$Keys[2]));
			if(Current($Keys) == $OrdersField['Value'])
				$Value = Next($Keys);
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
		#-------------------------------------------------------------------------------
		$Out[$ServicesField['Name']] = $Value;
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
//Debug(print_r($Out,true));
return $Out;
#-------------------------------------------------------------------------------
