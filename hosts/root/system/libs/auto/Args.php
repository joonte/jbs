function &Args(){
  #-----------------------------------------------------------------------------
  $Name = Md5('Args');
  #-----------------------------------------------------------------------------
  if(!IsSet($GLOBALS[$Name])){
    #---------------------------------------------------------------------------
    $GLOBALS[$Name] = $_GET;
    #---------------------------------------------------------------------------
    Array_Union($GLOBALS[$Name],$_POST);
  }
  #-----------------------------------------------------------------------------
  return $GLOBALS[$Name];
}
