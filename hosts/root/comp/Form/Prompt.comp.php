$__args_list = Array('LinkID','Text');
/******************************************************************************/
/******************************************************************************/
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Links = &Links();
#-------------------------------------------------------------------------------
$Object = &$Links[$LinkID];
#-------------------------------------------------------------------------------
$Object->AddAttribs(Array('OnMouseOver'=>SPrintF("PromptShow(event,'%s',this);",AddcSlashes(Str_Replace("\n",'<BR />',$Text),"\0\n\r\\\'"))));
#-------------------------------------------------------------------------------
if(!Comp_IsLoaded('Form/Prompt')){
	#-------------------------------------------------------------------------------
	$DOM = &$Links['DOM'];
	#-------------------------------------------------------------------------------
	$Script = new Tag('SCRIPT',Array('type'=>'text/javascript','src'=>'SRC:{Js/Prompt.js}'));
	#-------------------------------------------------------------------------------
	$DOM->AddChild('Head',$Script);
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load('Css',Array('Prompt'));
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	foreach($Comp as $Css)
		$DOM->AddChild('Head',$Css);
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return TRUE;
