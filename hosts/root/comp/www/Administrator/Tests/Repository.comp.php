$JavaScript = <<<EOD
function TreeSwitch(ElementID){
  //----------------------------------------------------------------------------
  var Element = document.getElementById(ElementID);
  if(!Element)
    return;
  //----------------------------------------------------------------------------
  var Style = Element.style;
  //----------------------------------------------------------------------------
  Style.display = (Style.display != 'none'?'none':'');
  //----------------------------------------------------------------------------
  FadeIn(Element);
}
EOD;
#-------------------------------------------------------------------------------
$DOM->AddChild('Head',new Tag('SCRIPT',$JavaScript));
