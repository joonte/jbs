$__args_list = Array('DaysRemainded','OnClick');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/

// если дней не осталось, то даты нет
if(!$DaysRemainded)
	return ($OnClick)?new Tag('SPAN',Array('OnClick'=>$OnClick,'style'=>'width:100%;display:block;'),'-'):'-';
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Attribs = Array('style'=>SPrintF('color:#%s;text-decoration:underline;',$DaysRemainded < 10?'FF0000':'1E2E07'));
#-------------------------------------------------------------------------------
if($OnClick)
	$Attribs['OnClick'] = $OnClick;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return new Tag('DIV',$Attribs,new Tag('SPAN',Date('d.m.y',Time()+$DaysRemainded*86400)),new Tag('SPAN',SPrintF('| %u дн.',$DaysRemainded)));
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

