if(Mb_StrLen($Domain) > $Length){
	#-------------------------------------------------------------------------------
	$Domain1 = SPrintF('%s...',Mb_SubStr($Domain,0,$Length));
}else{
	#-------------------------------------------------------------------------------
	$Domain1 = $Domain;
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
// ссылку показываем админу, или юзеру, если статус один из перечисленных
if($GLOBALS['__USER']['IsAdmin'] || In_Array($StatusID,Array('Active','ForTransfer','OnTransfer'))){
	#-------------------------------------------------------------------------------
	$A = new Tag('A',Array('target'=>'blank','href'=>SPrintF('http://%s.%s/',$Domain,$DomainZone),'title'=>'Перейти на сайт'),$Domain1);
	#-------------------------------------------------------------------------------
}else{
	#-------------------------------------------------------------------------------
	$A = SPrintF('%s.%s',$Domain,$DomainZone);
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $A;
