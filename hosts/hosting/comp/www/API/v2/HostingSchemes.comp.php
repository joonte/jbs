#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(Is_Error(System_Load('modules/Authorisation.mod')))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
/*
 * рассмотреть вариант сделать как в заказе на хостинг
 * $UniqID = UniqID('HostingSchemes');
 * #-------------------------------------------------------------------------------
 * $Comp = Comp_Load('Services/Schemes','HostingSchemes',$__USER['ID'],Array('Name','ServersGroupID'),$UniqID);
 * if(Is_Error($Comp))
 *   return ERROR | @Trigger_Error(500);
 *
 * */


$Out = Array();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Where = Array(
		'(`UserID` = @local.__USER_ID OR FIND_IN_SET(`GroupID`,@local.__USER_GROUPS_PATH))',
		'`IsActive` = "yes"',
		);
#-------------------------------------------------------------------------------
$HostingSchemes = DB_Select('HostingSchemesOwners','*',Array('Where'=>$Where,'SortOn'=>Array('SortID','PackageID')));
#-------------------------------------------------------------------------------
switch(ValueOf($HostingSchemes)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	return $Out;
case 'array':
	break;
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
foreach($HostingSchemes as $HostingScheme){
	#-------------------------------------------------------------------------------
	// загружаем XML
	$Fields = System_XML(SPrintF('config/Schemes.%s.xml',$HostingScheme['SchemeParams']['SystemID']));
	if(Is_Error($Fields))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	foreach(Array_Keys($Fields) as $Key){
		#-------------------------------------------------------------------------------
		//$Field = $Fields[$Key];
		#-------------------------------------------------------------------------------
		if(IsSet($HostingScheme['SchemeParams'][$Key])){
			#-------------------------------------------------------------------------------
			$Fields[$Key]['Value'] = $HostingScheme['SchemeParams'][$Key];
			#-------------------------------------------------------------------------------
		}else{
			#-------------------------------------------------------------------------------
			$Fields[$Key]['Value'] = "";
			#-------------------------------------------------------------------------------
		}
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
	$HostingScheme['SchemeParams'] = $Fields;
	#-------------------------------------------------------------------------------
	$Out[$HostingScheme['ID']] = $HostingScheme;
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Out;
